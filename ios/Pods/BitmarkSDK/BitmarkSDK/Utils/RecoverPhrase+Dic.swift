    internal static func bip39Word(index: Int, language: RecoveryLanguage) -> String {
        switch language {
        case .english:
            return bip39ENWords[index]
        case .chineseTraditional:
            return bip39CNTranditionalWords[index]
        }
    }
    
    internal static func indexOfWord(_ word: String, language: RecoveryLanguage) -> Int? {
        switch language {
        case .english:
            return bip39ENWords.firstIndex(of: word)
        case .chineseTraditional:
            return bip39CNTranditionalWords.firstIndex(of: word)
        }
    }
    
